// Retrieve all team div elements inside the container
const teamDivs = document.querySelectorAll('.container > div');

// Convert NodeList to array for easier manipulation
const teamArray = Array.from(teamDivs);

// Sort the array based on team rank
teamArray.sort((a, b) => {
    const rankA = parseInt(a.querySelector('.team').innerText);
    const rankB = parseInt(b.querySelector('.team').innerText);
    const teamRankA = parseInt(document.getElementById(`team${rankA}Rank`).innerText);
    const teamRankB = parseInt(document.getElementById(`team${rankB}Rank`).innerText);
    return teamRankA - teamRankB;
});

// Clear the container
const container = document.querySelector('.container');
container.innerHTML = '';

// Re-append the sorted team div elements to the container
teamArray.forEach(teamDiv => {
    container.appendChild(teamDiv);
});
